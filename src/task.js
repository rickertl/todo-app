export default class Task {
  constructor(
    title,
    description = "",
    dueDate = false,
    priority = "normal",
    complete = false
  ) {
    this.title = title;
    this.description = description;
    this.dueDate = dueDate;
    this.priority = priority;
    this.complete = complete;
  }

  setTitle(title) {
    this.title = title;
  }

  setDesc(description) {
    this.description = description;
  }

  setDueDate(date) {
    this.date = date;
  }

  setPriority(priority) {
    this.priority = priority;
  }

  setComplete() {
    this.complete = complete;
  }
}
